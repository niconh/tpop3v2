package ejercicio5;

public class SolucionEjercicio5 {
	
	private int gananciaMaxima;
	private int cantidadInstrucciones;
	
	public SolucionEjercicio5(int gananciaMaxima, int cantidadInstrucciones) {
		this.gananciaMaxima = gananciaMaxima;
		this.cantidadInstrucciones = cantidadInstrucciones;
	}

	public int getGanaciaMaxima() {
		return gananciaMaxima;
	}

	public int getCantidadInstrucciones() {
		return cantidadInstrucciones;
	}

}
