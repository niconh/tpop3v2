package ejercicio6;

public class SolucionEjercicio6 {
	
	private int numeroDeParticiones;
	private int cantidadInstrucciones;
	
	public SolucionEjercicio6(int numeroDeParticiones, int cantidadInstrucciones) {
		super();
		this.numeroDeParticiones = numeroDeParticiones;
		this.cantidadInstrucciones = cantidadInstrucciones;
	}

	public int getNumeroDeParticiones() {
		return numeroDeParticiones;
	}

	public void setNumeroDeParticiones(int numeroDeParticiones) {
		this.numeroDeParticiones = numeroDeParticiones;
	}

	public int getCantidadInstrucciones() {
		return cantidadInstrucciones;
	}

	public void setCantidadInstrucciones(int cantidadInstrucciones) {
		this.cantidadInstrucciones = cantidadInstrucciones;
	}
	
	

}
